void	ft_echo(char **args)
{
	int	i;

	i = 1;
	// флаги
	while (args[i])
	{
		ft_putstr_fd(args[i], 1);
		i++;
		if (args[i])
			ft_putstr_fd(" ", 1);
	}
	// \n при надобности
}
